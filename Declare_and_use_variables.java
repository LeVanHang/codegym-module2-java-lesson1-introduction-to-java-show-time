public class Declare_and_use_variables {
    public static void main(String[] args) {
        int i = 10; // khai báo biến i là kiểu int có giá trị là 10
        float f = 20.5f; //khai báo biến f có kiểu là float có giá trị là 20.5
        double d = 20.5; //khai báo biến d có kiểu dữ liệu là double có giá trị là 20.5
        boolean b = true; // khai báo biến b là kiểu boolean có giá trị là true
        char c = 'a'; //khai báo biến c có kiểu dữ liệu là char có giá trị là ký tự a
        String s = "Hà Nội"; //khai báo biến s có kiểu dữ liệu có giá trị là chuổi Hà Nội
        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("s = " + s);
    }
}
